public class Rectascension
{
    private int xx;
    private int yy;
    private int zz;

    public Rectascension(int x, int y, int z)
    {
        this.xx = x;
        this.yy = y;
        this.zz = z;
    }

    public int getXx()
    {
        return xx;
    }

    public void setXx(int x)
    {
        this.xx = x;
    }
    public int getYy()
    {
        return yy;
    }

    public void setYy(int y)
    {
        this.yy = y;
    }

    public int getZz()
    {
        return zz;
    }

    public void setZz(int z)
    {
        this.zz = z;
    }
}
